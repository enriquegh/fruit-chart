// Helper functions
export const getProduceByMonth = (monthNumber) => {
  const inSeason = {
    fruits: produceData.fruits.filter(fruit => fruit.seasons.includes(monthNumber)),
    vegetables: produceData.vegetables.filter(vegetable => vegetable.seasons.includes(monthNumber))
  };
  return inSeason;
};

export const getProduceBySeason = (seasonName) => {
  const seasonObj = seasons.find(s => s.name.toLowerCase() === seasonName.toLowerCase());
  if (!seasonObj) return { fruits: [], vegetables: [] };
  
  const seasonMonths = seasonObj.months;
  
  const inSeason = {
    fruits: produceData.fruits.filter(fruit => 
      fruit.seasons.some(month => seasonMonths.includes(month))),
    vegetables: produceData.vegetables.filter(vegetable => 
      vegetable.seasons.some(month => seasonMonths.includes(month)))
  };
  
  return inSeason;
};